// break - বিরতি দেওয়া
// case - কেস বা শর্ত
// catch - ধরা
// class - শ্রেণী
// const - স্থির
// continue - চালিয়ে যাওয়া
// covariant - সহ-পরিবর্তন
// default - ডিফল্ট
// deferred - বিলম্বিত
// do - করো
// dynamic - গতিশীল
// else - অন্যথায়
// enum - গুনতারণ
// export - রপ্তানি
// extends - সম্প্রসারিত
// extension - সম্প্রসারণ
// external - বাহ্যিক
// factory - ফ্যাক্টরি
// false - মিথ্যা
// final - চূড়ান্ত
// finally - শেষত
// for - জন্য
// Function - ফাংশন
// get - প্রাপ্তি
// hide - লুকানো
// if - যদি
// implements - বাস্তবায়ন করে
// import - আমদানি
// in - মধ্যে
// interface - ইন্টারফেস
// is - হলো
// late - দেরিতে
// library - গ্রন্থাগার
// mixin - মিশ্রণ
// new - নতুন
// null - শূন্য
// on - উপরে
// operator - অপারেটর
// part - অংশ
// rethrow - পুনরায় ছোড়া
// return - ফেরত
// set - সেট করা
// show - প্রদর্শন
// static - স্থির
// super - শ্রেষ্ঠ
// switch - স্যুইচ
// sync - সিঙ্ক্রোনাস
// this - এটি
// throw - ছোড়া
// true - সত্য
// try - চেষ্টা
// typedef - টাইপ ডিফাইন
