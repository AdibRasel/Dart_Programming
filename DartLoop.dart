void main() {
  // লুপ কাকে বলে:
  // লুপ একটি প্রোগ্রামিং কনস্ট্রাক্ট যা একটি নির্দিষ্ট শর্ত পূরণ না হওয়া পর্যন্ত কোডের একটি ব্লক বারবার কার্যকর করে।

  // লুপের প্রকারভেদ:
  // ১. for লুপ
  // ২. while লুপ
  // ৩. do-while লুপ
  // ৪. for-in লুপ
  // ৫. forEach লুপ

  // for লুপ:
  // for লুপ সাধারণত তখন ব্যবহার করা হয় যখন আপনি জানেন কতবার লুপটি চলবে।
  for (int i = 0; i < 5; i++) {
    print('for loop iteration: $i'); // রেজাল্ট: 0, 1, 2, 3, 4
  }

  // while লুপ:
  // while লুপ সাধারণত তখন ব্যবহার করা হয় যখন আপনি জানেন না কতবার লুপটি চলবে, কিন্তু শর্তটি পূরণ না হওয়া পর্যন্ত লুপটি চলবে।
  int j = 0;
  while (j < 5) {
    print('while loop iteration: $j'); // রেজাল্ট: 0, 1, 2, 3, 4
    j++;
  }

  // do-while লুপ:
  // do-while লুপ সাধারণত তখন ব্যবহার করা হয় যখন আপনি অন্তত একবার লুপটি চালাতে চান, তারপর শর্তটি চেক করতে চান।
  int k = 0;
  do {
    print('do-while loop iteration: $k'); // রেজাল্ট: 0, 1, 2, 3, 4
    k++;
  } while (k < 5);

  // for-in লুপ:
  // for-in লুপ সাধারণত তখন ব্যবহার করা হয় যখন আপনি একটি কালেকশন (যেমন লিস্ট) এর প্রতিটি উপাদান একবার করে অ্যাক্সেস করতে চান।
  var numbers = [1, 2, 3, 4, 5];
  for (var number in numbers) {
    print('for-in loop iteration: $number'); // রেজাল্ট: 1, 2, 3, 4, 5
  }

  // forEach লুপ:
  // forEach লুপ সাধারণত তখন ব্যবহার করা হয় যখন আপনি একটি কালেকশন (যেমন লিস্ট) এর প্রতিটি উপাদান একবার করে অ্যাক্সেস করতে চান এবং একটি ফাংশন প্রয়োগ করতে চান।
  numbers.forEach((number) {
    print('forEach loop iteration: $number'); // রেজাল্ট: 1, 2, 3, 4, 5
  });
}
